    private void Popular_overview(){
        Banco pop = new Banco();
        try {
            String [][] content = pop.Read_all();
            String ids = "";
            String nomes = "";
            String qtd_vendas = "";
            String qtd_estoques = "";
            String p_compras = "";
            String p_vendas = "";
            for (int i=0;i<content.length;i++){
                ids = ids + content[i][0]+"\n";
                nomes = nomes + content[i][1]+"\n";
                qtd_estoques = qtd_estoques + content[i][2]+"\n";
                qtd_vendas = qtd_vendas + content[i][3]+"\n";
                p_compras = p_compras + content[i][4]+"\n";
                p_vendas = p_vendas + content[i][5]+"\n";
            }
            coluna_id.setText(ids);
            coluna_nome.setText(nomes);
            coluna_estoque.setText(qtd_estoques);
            coluna_vendida.setText(qtd_vendas);
            coluna_compra.setText(p_compras);
            coluna_pvenda.setText(p_vendas);
        } catch (SQLException ex) {
            Logger.getLogger(painel_admin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
    
