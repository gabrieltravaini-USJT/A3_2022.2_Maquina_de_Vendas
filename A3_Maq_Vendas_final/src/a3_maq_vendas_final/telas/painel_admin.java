    private void AtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AtualizarActionPerformed
        if (search_field.getText().equals("Digite um ID para procurar")|search_field.getText().equals("")){
          Popular_overview();
        }
        else{
            Banco search = new Banco();
            try {
                Produto prod = search.Read(Integer.parseInt(search_field.getText()));
                coluna_id.setText(""+prod.getId());
                coluna_nome.setText(""+prod.getNome());
                coluna_estoque.setText(""+prod.getQuantidade_disponivel());
                coluna_vendida.setText(""+prod.getQuantidade_vendida());
                coluna_compra.setText(""+prod.getPreco_compra());
                coluna_pvenda.setText(""+prod.getPreco_venda());
            } catch (SQLException ex) {
                Logger.getLogger(painel_admin.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_AtualizarActionPerformed

